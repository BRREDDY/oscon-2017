# Authoring documentation for binary PowerShell modules is done using a XML format
# called MAML. We don't have a complete example for this module, but be aware that it exists.

# There's a simple way to author PowerShell help though, using Markdown. Microsoft 
# created a PowerShell module called PlatyPS that translates Markdown into MAML help.

# Install the PlatyPS module.
Install-Module -Name PlatyPS -Scope CurrentUser -Force

